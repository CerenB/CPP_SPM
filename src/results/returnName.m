function name = returnName(result)

  name = sprintf('%s_p-%0.3f_k-%i_MC-%s', ...
                 result.Contrasts.Name, ...
                 result.Contrasts.p, ...
                 result.Contrasts.k, ...
                 result.Contrasts.MC);

end
